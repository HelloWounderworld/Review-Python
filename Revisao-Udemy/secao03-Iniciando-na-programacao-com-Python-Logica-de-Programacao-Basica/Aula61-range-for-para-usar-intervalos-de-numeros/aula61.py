"""
For + Range
range -> range(start, stop, step)
"""
for i in range(10):
    print(i)
print('----------------------')
for i in range(0,10):
    print(i)
print('----------------------')
for i in range(5,10):
    print(i)
print('----------------------')
numeros = range(0, 100, 8)

for numero in numeros:
    print(numero)
print('----------------------')
for i in range(0,-10, -2):
    print(i)
